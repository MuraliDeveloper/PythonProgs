numbers = [1, 3, 4, 2]

# Sorting list of Integers in ascending
numbers.sort()
print("ascending")
print(numbers)


print("descending")
numbers.sort(reverse=True)
print(numbers)


print("reverse")
numbers.reverse()
print(numbers)

